                    <img src="../images/cat care/Cat Grooming.jpg" alt="Cat Grooming">
                    <h3>Grooming</h3>
                    <p>- Cats are naturally clean, but they still need regular care.<br><br>

                        <h5>Brushing</h5>
                        Short-haired cats: 1–2 times a week<br>
                        Long-haired cats: daily to prevent tangles and hairballs<br><br>
                        <h5>Nail trimming</h5>
                        Every 2–3 weeks to prevent overgrowth and scratching injuries<br><br>
                        <h5>Bathing</h5>
                        Only when necessary (cats usually groom themselves)<br><br>
                        <h5>Ears & eyes</h5>
                        Check for dirt, redness, or discharge<br><br>
                        <h5>Dental care</h5>
                        Brush teeth or give dental treats to prevent gum disease<br><br>

                        - Regular grooming keeps your cat comfortable and reduces shedding.</p>
                </div>
            </div>

            <div class="care-box">
                <div class="boxe">
                    <img src="../images/cat care/Cat Feeding.jpg" alt="Cat Feeding">
                    <h3>Feeding</h3>
                    <p>- Proper nutrition is essential for growth and overall health.<br><br>

                        Feed high-quality cat food (wet, dry, or mixed)<br><br>
                        Adjust diet based on age:<br>
                           <h5>Kittens</h5> → more frequent meals<br>
                           <h5>Adults</h5> → 2–3 meals daily<br><br>
                        Always provide fresh, clean water.<br>
                        Avoid harmful foods like chocolate, onions, garlic, and salty food.<br>
                        Don’t overfeed—follow portion guidelines to prevent obesity.<br>
                        <br>
                        - A balanced diet helps maintain energy, healthy fur, and strong immunity.</p>
                </div>
            </div>

            <div class="care-box">
                <div class="boxe">
                    <img src="../images/cat care/Cat Health.jpg" alt="Cat Health">
                    <h3>Health</h3>
                    <p>- Preventive care is key to a long, healthy life.<br><br>

                        Schedule regular vet check-ups (at least once a year)<br>
                        Keep vaccinations and deworming up to date<br>
                        Consider spaying/neutering<br><br>
                        <h6>Watch for warning signs:</h6>
                        - Loss of appetite<br>
                        - Vomiting or diarrhea<br>
                        - Weight loss<br>
                        - Lethargy<br>
                        - Behavior changes<br><br>

                        - Early detection of illness can prevent serious problems.</p>
                </div>
            </div>

            <div class="care-box">
                <div class="boxe">
                    <img src="../images/cat care/Cat Exercise.jpg" alt="Cat Exercise">
                    <h3>Exercise</h3>
                    <p>- Cats need daily movement and mental stimulation.<br><br>

                        Play with your cat 15–30 minutes daily<br><br>
                        <h6>Use toys like:</h6>
                        - Feather wands<br>
                        - Balls<br>
                        - Laser pointers (avoid shining in eyes)<br><br>
                        Provide a scratching post for claw health<br>
                        Cat trees or climbing spaces encourage natural behavior<br><br>

                        - Exercise helps prevent obesity, boredom, and destructive behavior.</p>
                </div>
            </div>

            <div class="care-box">
                <div class="boxe">
                    <img src="../images/cat care/Cat Litter.jpg" alt="Cat Litter">
                    <h3>Litter Box Care</h3>
                    <p>- Cats are naturally clean, so hygiene is important.<br><br>

                        Scoop waste daily<br>
                        Change litter completely once a week<br>
                        Use unscented litter (cats prefer it)<br>
                        Place the box in a quiet, low-traffic area<br><br>
                        <h6>Rule: 1 litter box per cat + 1 extra</h6></p>
                </div>
            </div>

