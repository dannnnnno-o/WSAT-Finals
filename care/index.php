                        <h3>Grooming</h3>
                    </div>
                    <p>➢➢ Cats are naturally clean, but they still need regular care.<br>

                        <strong>Brushing:</strong><br>
                        → Short-haired cats: 1–2 times a week<br>
                        → Long-haired cats: daily to prevent tangles and hairballs<br>
                        <strong>Nail trimming:</strong><br>
                        → Every 2–3 weeks to prevent overgrowth and scratching injuries<br>
                        <strong>Bathing:</strong><br>
                        → Only when necessary (cats usually groom themselves)<br>
                        <strong>Ears & eyes:</strong><br>
                        → Check for dirt, redness, or discharge<br>
                        <strong>Dental care:</strong><br>
                        → Brush teeth or give dental treats to prevent gum disease<br><br>

                        ➢ Regular grooming keeps your cat comfortable and reduces shedding.
                    </p>
                </div>
            </div>

            <div class="care-box">
                <div class="boxe">
                    <div class="img-warp">
                        <img src="../images/cat care/Cat Feeding.jpg" alt="Cat Feeding">
                        <h3>Feeding</h3>
                    </div>
                    <p>➢➢ Proper nutrition is essential for growth and overall health.<br>

                        → Feed high-quality cat food (wet, dry, or mixed)<br><br>
                        → Adjust diet based on age:<br>
                        <strong>Kittens:</strong> → more frequent meals<br>
                        <strong>Adults:</strong> → 2–3 meals daily<br><br>
                        → Always provide fresh, clean water.<br>
                        → Avoid harmful foods like chocolate, onions, garlic, and salty food.<br>
                        → Don’t overfeed—follow portion guidelines to prevent obesity.<br><br>
                        ➢ A balanced diet helps maintain energy, healthy fur, and strong immunity.
                    </p>
                </div>
            </div>

            <div class="care-box">
                <div class="boxe">
                    <div class="img-warp">
                        <img src="../images/cat care/Cat Health.jpg" alt="Cat Health">
                        <h3>Health</h3>
                    </div>
                    <p>➢➢ Preventive care is key to a long, healthy life.<br>

                        → Schedule regular vet check-ups (at least once a year)<br>
                        → Keep vaccinations and deworming up to date<br>
                        → Consider spaying/neutering<br><br>
                        <strong>Watch for warning signs:</strong><br>
                        → Loss of appetite<br>
                        → Vomiting or diarrhea<br>
                        → Weight loss<br>
                        → Lethargy<br>
                        → Behavior changes<br><br>

                        ➢ Early detection of illness can prevent serious problems.
                    </p>
                </div>
            </div>

            <div class="care-box">
                <div class="boxe">
                    <div class="img-warp">
                        <img src="../images/cat care/Cat Exercise.jpg" alt="Cat Exercise">
                        <h3>Exercise</h3>
                    </div>
                    <p>➢➢ Cats need daily movement and mental stimulation.<br>

                        Play with your cat 15–30 minutes daily<br><br>
                        <strong>Use toys like:</strong><br>
                        → Feather wands<br>
                        → Balls<br>
                        → Laser pointers (avoid shining in eyes)<br><br>
                        → Provide a scratching post for claw health<br>
                        → Cat trees or climbing spaces encourage natural behavior<br>

                        ➢ Exercise helps prevent obesity, boredom, and destructive behavior.
                    </p>
                </div>
            </div>

            <div class="care-box">
                <div class="boxe">
                    <div class="img-warp">
                        <img src="../images/cat care/Cat Litter.jpg" alt="Cat Litter">
                        <center>
                            <h3>Litter Box Care</h3>
                        </center>
                    </div>
                    <p>➢➢ Cats are naturally clean, so hygiene is important.<br>

                        → Scoop waste daily<br>
                        → Change litter completely once a week<br>
                        → Use unscented litter (cats prefer it)<br>
                        → Place the box in a quiet, low-traffic area<br>
                        <strong>Rule: 1 litter box per cat + 1 extra</strong><br>
                        → Keep the area around the litter box clean and dry<br>
                        → Use a litter mat to catch litter from your cat’s paws<br>
                        → Avoid placing food and water near the litter box<br>
                        → Clean the box with unscented soap to avoid strong smells<br>
                        → Replace the litter box if it becomes scratched or smelly over time<br>
                        → Observe your cat’s habits—changes in litter use can signal health problems
                    </p>
